package exercise.calculator.version1;

public class Maths {
    int result;

    public int addition(int a, int b){
        result = a + b;

        return result;
    }

    public int subtraction(int a, int b){
        result = a - b;

        return result;
    }

    public int multiplication(int a, int b){
        result = a * b;

        return result;
    }

    public int division(int a, int b){
        result = a / b;

        return result;
    }
}

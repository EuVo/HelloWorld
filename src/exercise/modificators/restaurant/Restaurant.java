package exercise.modificators.restaurant;

abstract public class Restaurant {

    //обязательная функция всех участников в ресторане это работа с заказами
    public abstract void orders();

    //обязательная функция всех участников в ресторане это место расположения
    public abstract void places();

    //обязательная функция всех участников в ресторане это выручка/оплата
    public abstract void payments();


}
